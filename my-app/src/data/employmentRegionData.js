export const normalizedRegionRatio = (() => {
  // 서울+경기 그룹 제외
  const filteredRegions = regionTotalData.filter(
    region => !regionGroups.수도권.includes(region.시도)
  );
  
  // 기타지역 합계 기준 정규화
  return filteredRegions.map(region => ({
    시도: region.시도,
    업체수: region.업체수,
    비율: Number(((region.업체수 / 기타지역합계) * 100).toFixed(2))
  })).sort((a, b) => b.비율 - a.비율); // 비율 내림차순 정렬
})();